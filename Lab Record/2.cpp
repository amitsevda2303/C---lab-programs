// Program to perform addition, subtraction, multiplication and division on 2 integer numbers

#include <iostream>
using namespace std;

int main()
{
  int a, b;
  cout << "Enter a number: ";
  cin >> a;
  cout << "Enter another number: ";
  cin >> b;
  cout << "a + b = " << a + b << endl;
  cout << "a - b = " << a - b << endl;
  cout << "a * b = " << a * b << endl;
  cout << "a / b = " << a / b << endl;
  return 0;
}